public class loop2 {
    public static void main(String[] args) {

        int b = 10;
        while (b > 0) {
            System.out.println(b);
            b--;
        }

        int c = 10;
        do {
            System.out.println(c);
            c--;
        } while(c > 0);
    }

}
// do while에서는 while와는 다르게 값을 한번 찍는다